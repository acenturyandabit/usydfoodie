function toBase64(int){
    let digits="0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ_";
    let result="";
    int=Math.floor(int);
    while (int>0){
        result+=digits[int%64];
        int-=int%64;
        int=int/64;
    }
    if (result.length==0)return "0";
    return result;
}


function guid(){
    return toBase64(Date.now())
}



